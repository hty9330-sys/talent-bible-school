def rate_for(month):
    r = RATE_STEPS[0][1]
    for mm, rr in RATE_STEPS:
        if month >= mm: r = rr
        else: break
    return r
ann_rate = np.array([rate_for(m) for m in months])
cash_ret = (1 + ann_rate/100.0) ** (1/12) - 1

g = np.full(N, np.nan); g[1:] = P[1:] / P[:-1] - 1

def perf(ret, cash=None):
    ret = np.asarray(ret, float); n = len(ret)
    cum = np.prod(1+ret) - 1
    cagr = (np.prod(1+ret)) ** (12.0/n) - 1
    eq = np.cumprod(1+ret); peak = np.maximum.accumulate(eq)
    mdd = (eq/peak - 1).min()
    mu, sd = ret.mean(), ret.std(ddof=1)
    sharpe0 = (mu*12)/(sd*np.sqrt(12)) if sd>0 else np.nan
    if cash is not None:
        ex = ret - np.asarray(cash,float); muE, sdE = ex.mean(), ex.std(ddof=1)
        sharpeE = (muE*12)/(sdE*np.sqrt(12)) if sdE>0 else np.nan
    else: sharpeE = np.nan
    return dict(n=n,cum=cum,cagr=cagr,mdd=mdd,sharpe0=sharpe0,sharpeE=sharpeE,vol=sd*np.sqrt(12))

def sig_single(t, look):
    if t-look < 0: return None
    return 1 if P[t] > P[t-look] else 0
def sig_multi(t, looks=(3,6,12), thr=2):
    if t-max(looks) < 0: return None
    c = sum(1 for L in looks if P[t] > P[t-L])
    return 1 if c >= thr else 0

START = 12
def run(signal_fn, cost_bps=0.0, use_cash=True):
    rets, idx, pos_prev, switches = [], [], 0, 0
    for t in range(START, N-1):
        s = signal_fn(t)
        if s is None: continue
        rt = g[t+1] if s==1 else (cash_ret[t+1] if use_cash else 0.0)
        if s != pos_prev:
            rt -= cost_bps/10000.0
            if len(idx) > 0: switches += 1
        rets.append(rt); idx.append(months[t+1]); pos_prev = s
    years = len(rets)/12.0
    return np.array(rets), idx, switches/years

bh_rets = g[START+1:N]; bh_cash = cash_ret[START+1:N]
